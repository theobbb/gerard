<div class="max-w-2xl text-xl text-balance">
	Born in France, Gérard Duboid studied graphic arts in Paris for five years. After graduating in
	1989, he crossed the Atlantic and worked as a graphic design consultant for various Acadian
	magazines and newspapers (New Brunswick, Prince Edward Island, Nova Scotia) as part of an
	international cooperation program.
	<br />
	<br />
	In 1991, he settled in Montreal and began his career as an illustrator for the press and publishing
	industries. His clients include The New York Times, for which he collaborated weekly for nearly two
	years on the Gray Matter column, The Wall Street Journal, Time Magazine—where he illustrated a column
	for two years—GQ, Rolling Stone, The New Yorker, The Washington Post, Le Monde, XXI, Le Nouvel Obs,
	Télérama, The Guardian, Playboy, Newsweek, Entertainment Weekly, Harper’s, The Atlantic, L’Actualité,
	Canada Post, Nike, Google, Isetan Department Store, Tim Robbins’ Actor’s Gang, the Théâtre des Célestins,
	as well as numerous publishing houses including Gallimard, Random House, Le Seuil, Actes Sud, Les Allusifs,
	Le Rouergue, Comme des géants, La Pastèque, The Folio Society, and creative agencies such as Paprika,
	BBDO, Sid Lee, and Pentagram.
	<br />
	<br />
	For nearly 30 years, his work has received many of the most prestigious distinctions, both for editorial
	illustration and books. In North America, he is the recipient of the Hamilton King Award, presented
	by the New York–based Society of Illustrators in recognition of his lifetime achievement. His images
	have also earned him four gold medals from this historic institution.
	<br />
	<br />
	For his books, he received the Bologna Ragazzi Award (Fiction category) in 2022 for Who Do the Clouds
	Belong To?, which also earned him the Governor General’s Award of Canada in 2021, as well as recognition
	as a Best Illustrated Book by The New York Times and the New York Public Library. He is also the recipient
	of the First Harry Black Award for Beyond the Forest (see full list below).
	<br />
	<br />
	On April 4, 2018, at the Ontario College of Art, Canada Post unveiled a Gérard DuBois postage stamp
	as part of the “Great Canadian Illustrators” series, honoring the excellence of his work.
	<br />
	In July 2022, he was awarded Illustrator of the Year by the Victoria and Albert Museum in London.
	<br />
	His illustrations have been featured in publications such as Les Arts Dessinés, 3×3, Communication Arts,
	Print Magazine, and Grafika, and he has been interviewed on Radio-Canada, in the series Contes pis raconte,
	as well as on TV5 Monde.
	<br />
	His work has been exhibited in Canada, France, Italy, and the United States, including in Chicago, Los
	Angeles, New York, Paris, Miami, Montreal, and Toronto.
	<br />
	His paintings, primarily acrylics, are held in private collections—including those of Rolling Stone
	Magazine, author Stephen King, and filmmaker Guillermo del Toro—as well as in the collections of the
	Victoria and Albert Museum in London and the Musée de l’Illustration Jeunesse in Moulins.
</div>

